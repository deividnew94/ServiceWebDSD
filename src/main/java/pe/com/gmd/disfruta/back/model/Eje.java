package pe.com.gmd.disfruta.back.model;

public class Eje {
	
	private int idEje;
	private String nomEje;
	private String desEje;
	private String imgEje;
	private String fecEje;
	private String inEstado;
	
	public String getFecEje() {
		return fecEje;
	}
	public void setFecEje(String fecEje) {
		this.fecEje = fecEje;
	}
	public int getIdEje() {
		return idEje;
	}
	public void setIdEje(int idEje) {
		this.idEje = idEje;
	}
	public String getNomEje() {
		return nomEje;
	}
	public void setNomEje(String nomEje) {
		this.nomEje = nomEje;
	}
	public String getDesEje() {
		return desEje;
	}
	public void setDesEje(String desEje) {
		this.desEje = desEje;
	}
	public String getImgEje() {
		return imgEje;
	}
	public void setImgEje(String imgEje) {
		this.imgEje = imgEje;
	}
	public String getInEstado() {
		return inEstado;
	}
	public void setInEstado(String inEstado) {
		this.inEstado = inEstado;
	}
	
}
