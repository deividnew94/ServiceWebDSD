package pe.com.gmd.disfruta.back.transfer.request;

public class ProveedorRequest {

	private int idProveedor;
	private String nomProveedor;
	private String desProveedor;
	private String urlProveedor;
	private String numTelefono;
	
	public int getIdProveedor() {
		return idProveedor;
	}
	public void setIdProveedor(int idProveedor) {
		this.idProveedor = idProveedor;
	}
	public String getNomProveedor() {
		return nomProveedor;
	}
	public void setNomProveedor(String nomProveedor) {
		this.nomProveedor = nomProveedor;
	}
	public String getDesProveedor() {
		return desProveedor;
	}
	public void setDesProveedor(String desProveedor) {
		this.desProveedor = desProveedor;
	}
	public String getUrlProveedor() {
		return urlProveedor;
	}
	public void setUrlProveedor(String urlProveedor) {
		this.urlProveedor = urlProveedor;
	}
	public String getNumTelefono() {
		return numTelefono;
	}
	public void setNumTelefono(String numTelefono) {
		this.numTelefono = numTelefono;
	}	
}
